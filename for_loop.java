//For_loop  Even num 1 to 100

class For_loop{

    public static void main(String args[])
    {
        int num;
        for(num=2; num<=100; num++)
        {
            if(num%2==0)
            {
                System.out.print(num);
            }

            System.out.print(" ");
        }
    }
}
